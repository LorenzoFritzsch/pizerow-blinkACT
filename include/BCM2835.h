/*
 * Broadcom BCM 2835 addresses as defined in the peripherals data sheet.
 */

#ifndef BCM2835
#define BCM2835

#define BCM2835_PERI_PHYSICAL 0x20000000
#define BCM2835_PERI_BUS 0x7e000000

#define BCM2835_GPIO_PHYSICAL_BASE (BCM2835_PERI_PHYSICAL + 0x200000)

#define BCM2835_GPFSEL0 BCM2835_GPIO_PHYSICAL_BASE
#define BCM2835_GPFSEL1 (BCM2835_GPIO_PHYSICAL_BASE + 4)
#define BCM2835_GPFSEL2 (BCM2835_GPIO_PHYSICAL_BASE + 8)
#define BCM2835_GPFSEL3 (BCM2835_GPIO_PHYSICAL_BASE + 0xc)
#define BCM2835_GPFSEL4 (BCM2835_GPIO_PHYSICAL_BASE + 0x10)
#define BCM2835_GPFSEL5 (BCM2835_GPIO_PHYSICAL_BASE + 0x14)

#define BCM2835_GPSET0 (BCM2835_GPIO_PHYSICAL_BASE + 0x1c)
#define BCM2835_GPSET1 (BCM2835_GPIO_PHYSICAL_BASE + 0x20)

#define BCM2835_GPCLR0 (BCM2835_GPIO_PHYSICAL_BASE + 0x28)
#define BCM2835_GPCLR1 (BCM2835_GPIO_PHYSICAL_BASE + 0x2c)

#endif // !BCM2835
